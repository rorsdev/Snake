import java.awt.image.BufferedImage;

abstract class Tile {

    protected int posX, posY;
    protected BufferedImage img;

    public int getPosX() {
        return posX;
    }

    public void setPosX(int posX) {
        this.posX = posX;
    }

    public int getPosY() {
        return posY;
    }

    public void setPosY(int posY) {
        this.posY = posY;
    }
}
